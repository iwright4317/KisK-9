	<h3>My dog doesn't share! </h3>
	<p>
Help! My dog doesn't share!  We have heard this complaint many times. When dogs aggressively 
guard their space and belongings, it is known as resource guarding. This is a serious behavior 
problem since it can lead to injury of other dogs or people. This behavior must be stopped and 
controlled before it becomes a problem. Aggressive dogs can be removed from a home by law 
enforcement so this is an important training issue. We see this most often when other dogs or 
children are living in the home and they become the targets of this unacceptable behavior. 
</p>
<h3>Too much of a good thing</h3>
<p>
Often we see it as a good thing if our dog is protective of the family or the home, but when 
this guarding behavior goes too far, it can be a problem. Your dog is a predator by nature. 
Predators have evolved to have to deal with limited food supply, so it is very natural for 
some dogs to aggressively guard their food, sleeping area, or toys by snarling, barking, 
snapping, or bitting. We sometimes see this behavior in dogs that have not been properly 
trained and socialized. We can improve this behavior even though it has roots in the natural 
traits of dogs. </p>
<h3>Myths</h3>
There are a lot of myths about resource guarding. Resource guarding is not due to your dog's 
need to dominate or that your dog has been spoiled. Resource guarding is an amplification of 
the dog's need for self-preservation. Resource guarding can not be fixed by trying to convince 
your dog that there is an overabundance of food.  Another myth is that resource guarding can 
not be fixed. Resource guarding can be improved through training and we can help. 
</p>
<h3>We can help</h3>
<p>
Some trainers use a trading method where you trade a less desirable food the dog already has 
with a portion of more desirable food. We have found that this is dangerous and should not be 
attempted. 
You should understand that is a very natural and normal behavior, but it is very dangerous and 
dogs that resource guard are unpredictable. Some dogs will not even let you near their bowl to 
give them better food as is sometimes suggested. And that in itself is extremely dangerous 
because a dog will maul you for food. In my video, the dog is growling and attacking 3 feet 
away from his food bowl. Encouraging people to put good food in there is extremely dangerous 
and will end up in bites. You need to work with a professional. I always teach “out” and “eat” 
and put more boundaries and structure around the food to show the dog it is not their food to 
guard, it’s mine. I have control of the food, when the dog can eat, 
and when I want the dog to get out of the bowl. A resource guarder will bite. 
</p>
