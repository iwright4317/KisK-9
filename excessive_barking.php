	<h3>My dog barks too much! </h3>
	<p>
You probably know that dogs usually bark because they want to communicate in a very 
emotional way, like to give a warning or because of high excitement. Dogs sometimes 
bark when someone knocks on the door or they are excited about getting food or treats. 
<br>
Dogs that seem to bark all the time and for seemingly no reason can be a problem for 
the family and neighbors. Law enforcement can get involved when neighbors have had 
enough. If your dog barks too much, we can help. 
<br>
We have seen that some dog breeds slightly tend to bark more than others, but any dog 
can have an excessive barking problem. Often over-barking is due to human causes. For 
instance, sometimes we teach our dogs to "speak" and then we realize maybe that wasn't 
a great idea! 
<br>
To fix an excessive barking problem we need to first figure out why your dog is 
barking. Is your dog demanding you to do something? Is your dog lacking confidence? 
Is your dog reacting to other dogs or noise? Once we figure out what is causing your 
dog to bark we can use behavior modification methods to improve the barking problem. 
</p>
<h3>My dog is excessively barking at other dogs during walks</h3>
<p>
This is a common problem. First, we should confirm your dog is OK with going on walks 
with a collar and leash. If not, you should read our article on leash training. The 
collar and leash are helpful for when you need to indicate to your dog when the barking 
becomes excessive. It is important, though, that we use correction with compassion and as 
little as possible, 
as needed. Before we use correction for excessive barking, we need to try to convince 
your dog that other dogs are not a threat. We can be very helpful to you since we can 
provide "new" dogs for your dog to interact with. We can schedule these positive 
interactions to teach your dogs that it is OK to meet other dogs on walks. It is 
important you stay calm during these interactions. </p>
<h3>My dog barks at me to demand something</h3>
<p>
If your dog barks at you to demand something from you, it is your dog's attempt at 
dominance over you. Is it important that you not encourage or allow this. If your dog 
barks at you to demand something and you know what your dog wants, do not give it to 
your dog. You can give that thing to your dog much much later when your dog is being 
good. If you allow your dog to think it can dominate you, he will continue to do so. 
This is would be unhelpful to any behavior correction training with your dog. 
</p>
<h3>Your dog may be stressed</h3>
<p>
Your dog can have many of the same emotions we have. Dogs can get stressed over 
things that we may not be aware of. A new member of the family, like another pet or 
person, can cause stress on your dog which may cause your dog to bark more than usual. 
Dogs love routine and any interruption in routine can cause stress in your dog.
</p>
<h3>Your dog may be in pain</h3>
<p>
If there seems to be no cause for your dog's barkyness, you should consider taking 
your dog to a veterinarian for a health check. Your dog may have a digestive system 
issue or some other problem that is causing discomfort.
