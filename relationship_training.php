<h3>Read your dog</h3>
<p>
Relationship-based training focuses on teaching you how to read your dog's body 
language and other ways your dog uses to communicate with you. It is proven to 
improve dog and owner relationship and communication. When we fully understand 
our dogs, it is much easier to have a better relationship. Since dogs are unique, 
the meaning of particular body language actions can vary from dog to dog. For 
instance, tail wagging does not always mean your dog is happy. We see tail 
wagging as more of an indication of a  higher level of interest. A low tail wag 
might mean the dog is afraid. I high tail wag might mean an attack is coming. 
A random tail wag is probably an indication of happiness.  We look at the entire 
dog to get an idea of how your dog is feeling. The play bow, where the front is 
down and the rear is up, seems to be a very reliable indication of invitation to 
play. </p>
