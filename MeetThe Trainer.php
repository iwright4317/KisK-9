	Hi guys! My name is Kiley and I am a professional dog trainer in the Virginia Beach area. I have been working with dogs for years and have been training for about 3 years. My training style is relationship-based, personalized & tailored to fit you and your dog’s lifestyle. 
	I also use balanced training, which is using both positive and adverse consequences for behavior. We limit adverse consequences to the absolute minimum and try to make the training feel like dog play! Another key part of my training is the first consultation is free. This first consultation is needed so that we can get an idea of what you want this training to achieve and what we recommend after meeting your dog. Please use our <a href="contact.php">contact form </a> to schedule your free consult today.</p>
	<h3>You and your dog’s relationship</h3>
<p>My training starts from the root of you and your dog’s relationship to really dig deep and correct behaviors where they begin and reconstruct you and your dog’s relationship. I work with all my clients one on one, or I come to your home when you are working or busy and train your dog(s). Upon meeting you, we do a free initial consult that is about 4 pages so I can really get the full picture and nothing is left out and you know that I know exactly what you want from training. 
</p>
<h3>Making dog training more affordable</h3>
<p>
I started training dogs after working with them for years and realizing that untrained dogs are the norm, and trained dogs were few and far between. I wanted to make one on one training more affordable for people. I have a rescue German shepherd who was rehomed 3 times before coming to live with me, and that was definitely a life changing experience that eventually led me to professionally training dogs, after fully educating myself on dog behavior.
I know to sometimes dogs are removed from the home because of bad behavior that may have been corrected through training.
I don't want the cost of training to be a barrier and prevent dogs from getting the training they need. Good training benefits the dog, the whole family and the people and animals around us. I am very proud of the success I have had in improving dog behavior and I invite you to take a look at <a href="testimonials.php" >my testimonials page </a>to read what my customers have to say about my training. 
</p>
