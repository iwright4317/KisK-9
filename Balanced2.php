	<h3>Training with compassion</h3>
	<p>Our balanced 
	training is a reward-first method and we use as few aversive consequences as needed and with as much compassion as possible. </p>
	<p>
		<h3>At the start</h3>
We start your dog's behavior education using rewards for good behavior. We want a good first impression! We want your dog to understand and strive for the goal of getting rewards. Training should be fun for your dog and we want your dog to first learn about the training activities without concern for mistakes. We only gradually introduce training tools. <br>We like to start any training session with some playtime. It is a chance to burn off any excess energy and get your dog into a happy mood. Playtime can also serve as a reward to your dog if play is the highest reward for your dog. We gradually introduce training exercises with the idea that your dog won't notice playtime vs training time. 
</p>
<p>
	<h3>Keep it positive</h3>
	Before we use a correction tool like a training collar, we first get your dog to understand the idea that the pressure the collar causes can be avoided. We let your dog experience all of the behaviors that avoid the pressure and also those behaviors that cause the pressure. We stress compassion and common sense. We achieve the best success by giving your dog a chance to understand how training works. If we use a training collar, we start by using the lowest possible setting first. We continue to guide your dog and encourage those behaviors that lead toward rewards. Tools like a training collar are used only for the more stubborn behaviors. </p>
<p>
	<h3>Putting it together</h3>
	The main idea 
though is to tailor any training to the uniqueness of your dog and use encouragement as much as possible. At Ki’s K-9 we strongly believe in balanced training. Balanced training has been proven to be the best way to train dogs. We reward positive behavior, and also modify inappropriate and negative behavior with humane and gentle corrections. Positive only training does not work due to the fact that many dogs will learn to ignore you unless you have something they want. For example, if your dog is running into the street or into a dangerous situation, you want to have full verbal control over your dog regardless if you have treats or not. This is when balanced training pays off. With a humane and gentle consequence in place, 
in real life, dangerous situations where you need your dog’s attention you will have it with our training methods. At Ki’s K-9, we are open to using tools but we do not use tools with every dog. Some dogs can succeed without using tools, but some dogs do need tools. The goal of Ki’s K-9 is to achieve a well rounded, well-behaved dog and we are able to do that in a gentle, humane way. For questions about balanced training, please call us at 757-750-1517 or 
email Kisk9training@outlook.com or <a href="contact.php">use our online form </a></p>
	                     
