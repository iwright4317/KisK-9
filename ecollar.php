			<h2>Dog training using an e-collar</h2>
			We provide top quality Virginia Beach dog training and for cities in the 757. 
E-collars have been around for a long time and can be very 
humane and effective to gently stop your dog's bad behavior. 
Quality collars have over 100 levels so you can preciously adjust the 
stimulation level based on the sensitivity of your dog. The purpose of the 
stimulation is to provide an annoying sensation to stop bad behavior and reinforce good behavior.
<h2>We offer e-collar training</h2>
Before e-collar training, we will guide you and show you how to properly use your e-collar 
so it is completely gentle, humane, safe, and effective. We always use it on ourselves and 
show you as well to ensure it is used correctly and at the right stimulation level. 
When used correctly, an e-collar is a fantastic 
tool and a great aid in training! Please contact us for a <a href=contact.php>free consultation</a> to 
see if e-collar training is right for your dog!
<h2>Find the lowest setting</h2>
If we use an E-collar, we introduce the collar to your dog using a 
low setting and the "tapping" method to get an idea of your dog's response to the collar. 
The idea is to see the lowest setting that will get your dog's attention. We start with the 
lowest setting and start tapping on the stimulation button. I very slowly increase the 
level until we just start to see a reaction such as the ears perking up. That lets me know 
the level that we can start training.
<h2>Your dog is unique</h2>
Your dog is unique and so finding the lowest setting needed to start training your dog is 
very important.  Also, depending on how your dog responds to training, the starting level 
may need further adjustment. The important thing is to always try to use the lowest setting 
that achieves the training goal. Often the setting can be lowered after your dog understands 
the training process and rewards reinforce the good behavior.
<h2>Reinforcing good Behavior</h2>
E-collars should never be used to teach a new behavior. They can be 
used to reinforce a learned good behavior to make that behavior more reliable or to get a better 
reaction. 
<h2>Stopping bad behavior</h2>
E-collars are very helpful to stop bad behaviors, especially when you are not in sight or at a far distance. Timing is very important so that the stimulation occurs at the time of the bad behavior so your dog associates the bad behavior with the annoying sensation. We have found that e-collars are especially useful when correcting aggressive behavior or resource guarding. Sometimes the correction needs to be fast and firm to prevent injury to people or other animals. Resource guarding is overly aggressive guarding of possessions like food bowls. An e-collar adds another tool to use when correcting aggressive behavior.  
