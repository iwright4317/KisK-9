                            <h3>Balanced Training</h3>
<p>Your dog wants you to have the leadership role and wants to please you by behaving correctly. 
	Rewards and negative consequences must occur at the time of the behavior because otherwise, 
	your dog will not connect their behavior to your response. We never use hitting or 
	shouting as negative consequences; we use startle methods such as a quick leash and 
	collar tension. 
Your dog is unique and the best training for your dog will be understood from our free initial 
consultation. If you live in Virginia Beach or nearby, please <a href=contact.php>contact us</a> today. 
We have found dogs vary on what rewards motivate them. It's best to observe your dog  
to figure this out. We would love to meet you and your dog to talk about improving your dog's 
behavior.</p><p>
	<h3>Training has changed</h3>
	Over the years, dog training has been a sort-of pendulum swinging between punishment-only training and reward-only training. 
Many years ago, training based on aversive consequences was the norm. This training used a lot of force and caused a lot of stress on the dog. The dog had to figure out how to avoid stressful consequences. The only "reward" was not experiencing these bad consequences. 
Around the 1980s, reward-only training gained popularity. Here, bad behavior is ignored, and good behavior is rewarded. This training was not very effective. Behavior was not very consistent and it was a problem when rewards for good behavior were not always provided.</p> 
<p><h3>Modern training</h3>
About a decade ago, after it became clear that reward-only is not effective, the pendulum found the middle, and a more balanced approach to dog training evolved that included a limited amount of aversive consequences for bad behavior along with rewards for good behavior. We use as little aversive consequences as needed and with as much compassion as possible. So, "balanced" is a bit of a mislabel since we do not mean equal amounts of positive and aversive consequences. Our approach is to use rewards such as affection and treats first to establish a good rapport with your dog. We have seen some dogs need no aversive consequences to achieve all training goals. That is truly great when that happens, but some dogs do need at least a little aversive consequences so they understand what is being asked of them. We talk with you and work out the best approach for your dog and adjust as your dog makes progress. 
