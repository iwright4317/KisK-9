         	<h1>Virginia Beach Dog Training</h1>
			<h3>Interact with your dog</h3>
<p>Relationship-based training is involved with how you interact with your dog and understanding your dog's point of view. It does not mean letting your dog do whatever they want. Relationship-based training is similar to teaching a child. Just as you would pay attention to a child as they are learning a topic to see how well they understand the topic, you should pay attention to your dog when teaching your dog a new behavior so see how well your dog is understanding the behavior being trained. We don't want to progress too quickly. It is important that the training be done in steps so that we only build on behaviors that have been reliably trained.
</p>
<h3>Make sure your dog is ready</h3>
Just like people, dogs need to be receptive to training before real training can happen. Dogs are easily distracted, so it is best to teach a new behavior inside, in their own home, with minimal distractions. Make sure you pay attention to how your dog may be feeling. Is your dog thirsty, hungry, hurt, or not feeling well? If so, take care of those issues before attempting a training session. Also, we usually have some playtime or take a quick walk to burn-off some of our dog's energy before any real training starts. This is a good time for you to pay attention to your dog's body language. Is your dog telling you he seems content, or is he indicating agitation or fear? We like to look at the overall body language indication your dog is showing to figure out what your dog is telling us. We want an indication of alertness but not overly excited. If your dog seems agitated or afraid, we need to correct the cause of this fear or agitation before we can proceed with any training. 
</p>
<h3>Teach in steps</h3>
<p>
A big part of this training is to teach your dog in steps, starting with a very basic behavior. Dogs need a lot of repetitions before that can be counted on the perform a learned behavior reliably. It may take 50 successful responses to a command before the command is truly learned. A good example of teaching your dog in steps is training the "Stay" command. We start training the "Stay" command, inside your dog's home so that there a few distractions. We repeat that training a lot to make sure it is reliable before moving outside. It would not be fair to your dog to try to first teach the "Stay" command at a park when other dogs and squirrels are running around. 
<h3>Putting it together</h3>
<p>
When teaching a new behavior, make sure your dog seems undistracted and calm. Have some playtime, then have some quiet time. Start indoors and minimize distractions. Teach the behavior over and over, dozens of times. After your dog is getting it reliably, start to increase the distractions and make sure your dog stays focused and behaves correctly. Dogs, like people, have an attention span. If we try to go beyond your dog's attention span, we will not gain any training benefit. Dogs sometimes tell us their attention span has elapsed by laying down or just not responding to commands. We need to not push your dog too much beyond his attention span and take-up the training at another time.

</p>
